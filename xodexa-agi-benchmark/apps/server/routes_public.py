"""
apps.server.routes_public
===========================
The LIVE site data: the public leaderboard and dashboard built ONLY from real,
verified, scored runs. Empty until real runs exist — the live pages never show demo
or hardcoded numbers (the demo set lives separately under the frontend DEMO menu).
Catalog data (families/generators/anchors/datasets) is real and served from static
data files generated by scripts/export_frontend_data.py.
"""

from __future__ import annotations

import json
from pathlib import Path

from fastapi import APIRouter, Depends
from sqlalchemy.orm import Session

from apps.server.db import get_db
from apps.server.models import Report, User, WebRun

from xodexa import families, generators
from xodexa.calibration import rank_upper_bound

router = APIRouter(prefix="/api", tags=["public"])
_REPO = Path(__file__).resolve().parents[2]


def _entry(run: WebRun, rep_json: dict, username: str) -> dict:
    ar = rep_json.get("agi_readiness", {})
    fm = rep_json.get("frontier_metrics", {})
    return {
        "run_id": run.id, "model": run.model_name, "provider": run.provider,
        "submitted_by": username, "family": run.family,
        "xodexa_score": run.xodexa_score, "grade": run.grade,
        "agi_level": run.agi_level, "agi_level_name": ar.get("level_name"),
        "agi_index": run.agi_index, "accuracy": run.accuracy,
        "calibration_error": run.calibration_error,
        "ci": fm.get("accuracy_ci95", 0.0),
        "subscores": ar.get("subscores", {}),
        "family_scores": rep_json.get("family_scores", {}),
        "contamination_risk": rep_json.get("contamination_risk"),
        "time_horizon": rep_json.get("time_horizon", {}).get("estimated_task_horizon"),
        "created_at": run.created_at.isoformat() if run.created_at else None,
    }


@router.get("/leaderboard")
def leaderboard(db: Session = Depends(get_db)):
    runs = (db.query(WebRun)
            .filter(WebRun.status == "scored", WebRun.visibility == "public")
            .order_by(WebRun.xodexa_score.desc()).limit(500).all())
    entries = []
    for run in runs:
        rep = db.query(Report).filter_by(run_id=run.id).first()
        if not rep:
            continue
        user = db.get(User, run.user_id)
        entries.append(_entry(run, rep.report_json, user.username if user else "unknown"))
    # HLE-style rank upper bound on accuracy ± CI
    if entries:
        ranked = rank_upper_bound([{"model": e["run_id"], "score": e["accuracy"] or 0,
                                    "ci": e["ci"] or 0} for e in entries])
        rmap = {r["model"]: r["rank_ub"] for r in ranked}
        for e in entries:
            e["rank_ub"] = rmap.get(e["run_id"], 1)
    return {"benchmark_version": "1.0.0", "live": True,
            "count": len(entries), "entries": entries}


@router.get("/dashboard")
def dashboard(db: Session = Depends(get_db)):
    scored = db.query(WebRun).filter(WebRun.status == "scored",
                                     WebRun.visibility == "public").all()
    n = len(scored)
    models = sorted({r.model_name for r in scored})
    avg_score = round(sum(r.xodexa_score or 0 for r in scored) / n, 1) if n else None
    avg_agi = round(sum(r.agi_index or 0 for r in scored) / n, 4) if n else None
    total_runs = db.query(WebRun).count()

    summary = _read_json("datasets/SUMMARY.json") or {}
    seed_total = 0
    for grp in ("releases", "focused_packs", "family_minis"):
        seed_total += sum(p.get("task_count", 0) for p in summary.get(grp, []))
    seed_total += (summary.get("dynamic", {}) or {}).get("sample_variants", 0)

    return {
        "live": True,
        "kpis": {
            "verified_runs": n,
            "total_runs": total_runs,
            "models_evaluated": len(models),
            "average_xodexa_score": avg_score,
            "average_agi_index": avg_agi,
            "task_families": len(families.FAMILY_KEYS),
            "generators": len(generators.list_generators()),
            "seed_tasks": seed_total,
        },
        "models": models,
        "empty": n == 0,
    }


def _read_json(rel: str):
    p = _REPO / rel
    if p.exists():
        try:
            return json.loads(p.read_text(encoding="utf-8"))
        except Exception:
            return None
    return None
